    private void updateTable() {
        tableModel.setRowCount(0); // clear table
        for (Book b : books) {
            String status = b.isBorrowed ? "Borrowed" : "Available";
            tableModel.addRow(new Object[]{b.isbn, b.title, b.author, b.year, status});
        }
    }

