    private void addBookBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookBttnActionPerformed
        String isbn = isbnTxtField.getText();
        String title = titleTxtField.getText();
        String author = authorTxtField.getText();
        String Year = yearpubTxtField.getText();
        int year;

        if (bookMap.containsKey(isbn)) {
            JOptionPane.showMessageDialog(rootPane, "Book already exists!");
            return;
        }

        if (isbn.isEmpty() && title.isEmpty() && author.isEmpty() && Year.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please input the following to add a book:\nISBN\nTitle\n"
                    + "Author\nYear Published", "", JOptionPane.ERROR_MESSAGE);
        } else if (isbn.isEmpty() || title.isEmpty() || author.isEmpty() || Year.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Input Correctly", "", JOptionPane.ERROR_MESSAGE);
        }

        try {
            year = Integer.parseInt(yearpubTxtField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Year must be in numbers!");
            return;
        }

        Book book = new Book(isbn, title, author, year);
        books.add(book);
        bookMap.put(isbn, book);
        updateTable();
        JOptionPane.showMessageDialog(rootPane, "Book Added Successfully");
        clearFields();
    }//GEN-LAST:event_addBookBttnActionPerformed

    private void borrowBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrowBttnActionPerformed
        String isbn = brrwrtrnTxtField.getText();
        Book b = bookMap.get(isbn);

        if (b != null && !b.isBorrowed) {
            b.isBorrowed = true;
            transactionStack.push(b);
            JOptionPane.showMessageDialog(rootPane, "Book successfully borrowed!");
            updateTable();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Book unavailable or not found!");
        }
    }//GEN-LAST:event_borrowBttnActionPerformed

    private void returnBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnBttnActionPerformed

        String isbn = brrwrtrnTxtField.getText();
        Book b = bookMap.get(isbn);

        if (b != null && b.isBorrowed) {
            b.isBorrowed = false;
            transactionStack.push(b);
            JOptionPane.showMessageDialog(rootPane, "Book Returned!");
            updateTable();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Invalid Return!");
        }
    }//GEN-LAST:event_returnBttnActionPerformed

    private void undoBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoBttnActionPerformed
        if (!transactionStack.isEmpty()) {
            Book b = transactionStack.pop();
            b.isBorrowed = !b.isBorrowed;
            JOptionPane.showMessageDialog(rootPane, "Last transaction undone!");
            updateTable();
        } else {
            JOptionPane.showMessageDialog(rootPane, "No transaction to undo!");
        }
    }//GEN-LAST:event_undoBttnActionPerformed

